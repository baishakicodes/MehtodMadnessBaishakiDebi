package BaishakiDebi;

public class BaishakiDebiLib {
    public static int multTable(int base, int range){
        String newString= "";
        for (int i=0; i<range+1; i++){
            return base*i;
            newString = newString + base + "*" + i + "=" + (base*i);
            System.out.println(newString);

        }
    }
}
